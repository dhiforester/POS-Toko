<h4>
    4. Kelola Data Barang
</h4>
<p>
    Pengelolaan data Barang meliputi aspek validitas informasi nama/merek dagang Barang yang bersangkutan, satuan jual, 
    stok yang tersedia, dan harga. Masing-masing komponen didukung oleh fitur yang dirancang mempermudah pengelolaan baik 
    penulisan data maupun aksesibilitas informasi. Setiap fitur pada halaman data Barang dijelaskan sebagai berikut:
</p>
<p>
    <b>Input Data Barang</b>
    <ul>
        <li>Masuk ke menu <b>Data Barang</b>.</li>
        <li>Pada bagian atas tabel, pilih <b>Tambah Barang</b>.</li>
        <li>Sistem akan menampilkan form isian. Anda hanya perlu melakukan pengisian data Barang sesuai informasi yang ada</li>
        <li>Pastikan anda mengisi form tersebut dengan benar. Apabila terdapat kesalahan maka sistem akan menampilkan pesan</li>
        <li>Pilih tombol simpan untuk memulai proses penyimpanan data pada database.</li>
        <li>Pada halaman depan juga tersedia fitur untuk melihat detail maupun melakukan perubahan dan menghapus data.</li>
    </ul>
    <b>Kelola data Batch</b>
    <ul>
        <li>Pilih salah satu item Barang dengan menekan tombol icon file detail pada data Barang.</li>
        <li>Pilih tombol <i>Show Batch</i>.</li>
        <li>Pada bagian atas terdapat kolom form untuk menambah data nomor batch Barang anda.</li>
        <li>Untuk melakukan pencarian cepat, pada halaman data Barang pilih tombol batch</li>
        <li>Isi kata kunci pencarian kemudian mulai pencarian, apabila data terdata dengan baik maka akan ditampilkan pada popup tersebut.</li>
    </ul>
</p>