<h4>
    2. Data Akses Dan Login
</h4>
<p>
    Pastikan bahwa aplikasi anda diamankan dengan prosedur akses yang baik sehingga setiap petugas
    dapat menggunakan aplikasi sesuai hak dan kewajibannya. Berikut ini adalah tahapan yang bisa anda lakukan 
    dalam rangka melakukan pengelolaan data akses tersebut.
</p>
<p>
    <ul>
        <li>Masuk sebagai admin dengan menggunakan akun yang diberikan pada saat pertama kali menggunakan aplikasi.</li>
        <li>Apabila login berhasil, masuk ke menu akses (Tersedia pada menu kiri untuk tampilan dekstop).</li>
        <li>Pilih tombol tambah user untuk menambahkan data akses petugas kasir anda.</li>
        <li>Isi data username dan password kemudian level akses yang dikehendaki, kemudian pilih tombol simpan.</li>
        <li>Pada halaman data akses juga anda dapat melakukan perubahan akun akses dengan memilih tombol edit, 
            atau menghapus data akses dengan memilih tombol hapus.
        </li>
    </ul>
</p>