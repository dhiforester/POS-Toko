<h4>
    3. Penjelasan Dashboard Aplikasi
</h4>
<p>
    Pada saat pertama kali masuk ke halaman dashboard aplikasi, anda akan disuguhkan dengan tampilan data. Perlu diingat bahwa 
    data-data tersebut dapat membantu anda dalam melakukan pengelolaan toko menjadi lebih baik dan akses terhadap informasi 
    penting tersebut menjadi lebih mudah. Berikut ini penjelasan tampilan dashboard yang perlu anda ketahui. 
</p>
<p>
    <ul>
        <li><b>Data barang hampir habis</b> merupakan informasi Barang yang secara standar memiliki stok berada dibawah 100 item.</li>
        <li><b>Jumlah Transaksi Penjualan</b> Informasi ini menunjukan jumlah transaksi penjualan pada bulan ini.</li>
        <li><b>Jumlah Transaksi Pembelian</b> Informasi ini menunjukan jumlah transaksi pembelian pada bulan ini.</li>
        <li><b>Jumlah Pendapatan</b> Informasi ini menunjukan jumlah pendapatan dalam satuan rupiah.</li>
        <li><b>Jumlah Pengeluaran</b> Informasi ini menunjukan jumlah pengeluaran berupa pembelian Barang dalam satuan rupiah.</li>
        <li><b>Item Barang</b> Informasi ini menunjukan jumlah item Barang yang terdaftar.</li>
        <li><b>Petugas Toko</b> Informasi ini menunjukan jumlah petugas yang memiliki hak akses.</li>
        <li><b>Utang Usaha</b> Informasi ini menunjukan jumlah utang usaha yang dibebankan pada Toko.</li>
        <li><b>Piutang Usaha</b> Informasi ini menunjukan jumlah piutang usaha yang dibebankan pada pihak lainnya.</li>
        <li><b>Kontak Bantuan</b> apabila anda mengalami kendala atau temuan error maka bisa menghubungi pihak pengembang.</li>
    </ul>
</p>