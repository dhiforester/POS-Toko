<h4>
    5. Transaksi Kasir
</h4>
<p>
    Petugas kasir dapat mempergunakan berbagai fitur yang ada pada modul kami. Fitur-fitur tersebut diantaranya adalah:
    Tambah data rincian barang, menambahkan rincian barang melalui scan barcode, pengaturan PPN dan Diskon, 
    menambahkan member atau konsumen untuk pemberian point belanja. Lebih jelas dapat dilihat pada uraian berikut ini.
</p>
<p>
    <b>Input Rincian Barang</b>
    <ul>
        <li>Pada halaman kasir, pilih tombol <i>Add</i>.</li>
        <li>pilih standard harga yang akan digunakan untuk transaksi .</li>
        <li>Cari item barang dengan mengisi nama atau kode barang</li>
        <li>Pilih salah satu item dengan mengisi jumlah dan tambahkan</li>
        <li>Item barang secara otomatis akan masuk ke rincian transaksi.</li>
        <li>Apabila anda ingin merubah rincian tersebut (edit atau hapus) pada tabel rincian sudah tersedia tombol yang bisa digunakan untuk melakukan perubahan tersebut.</li>
    </ul>
    <b>Scan Barcode Barang</b>
    <ul>
        <li>Pilih tombol <i>Scan</i> pada halaman kasir.</li>
        <li>Pastikan kursor berada pada form scan barcode.</li>
        <li>Lakukan pemindaian dengan menggunakan perangkat <i>Scaner</i> yang anda miliki.</li>
        <li>Apabila berhasil maka item barang akan masuk pada tabel daftar rincian transaksi</li>
    </ul>
    <b>Simpan Transaksi</b>
    <ul>
        <li>Pilih tombol <i>Simpan</i> pada halaman kasir.</li>
        <li>Apabila anda ingin menghitung jumlah uang kembalian, masukan nominalnya pada form jumlah uang.</li>
        <li>Apabila anda ingin melakukan input data transaksi dengan pembayaran Utang/Piutang pilih item tersebut dan form pembayaran akan aktif.</li>
        <li>Isikan jumlah nominal pembayaran dan secara otomatis sistem akan membacanya sebagai transaksi dengan pembayaran utang atasu piutang</li>
        <li>Apabila anda ingin menambahkan member, cari nama atau kode member tersebut pada form member.</li>
        <li>Cheklis salah satu member yang akan di input kemudian tekan tombol simpan.</li>
    </ul>
</p>