<h4>
    1. Instalasi Aplikasi dan Webserver
</h4>
<p>
    Sistem yang dibangun merupakan aplikasi berbasis web sehingga membutuhkan webserver baik diimplementasikan secara online
    maupun offline pada komputer lokal. Sebelum lebih jauh memahami cara melakukan instalasi aplikasi ini maka 
    dibutuhkan spesifikasi minimum yang memenuhi syarat dalam menunjang berjalannya aplikasi ini.
</p>
<p>
    <ul>
        <li>Aplikasi Webservice Lokal (Wamp Server, Xampp, Apache, dll).</li>
        <li>Menggunakan Versi PHP 7.0.3.3.</li>
        <li>DBMS Versi MySql 5.7.24</li>
        <li>Sistem Operasi Minimum Win 7 32/64 bit</li>
        <li>Browser App (Direkomendasikan menggunakan mozila firefox versi terbaru)</li>
    </ul>
</p>
<p>
    Setelah perangkat anda memenuhi syarat instalasi, anda hanya cukup melakukan copy file ke directory pada webservice 
    yang anda gunakan tersebut. Tidak lupa melakukan import database yang telah kami sediakan dengan ekstensi nama Toko.sql
</p>
<p>
    Lakukan uji coba koneksi dengan membuka perambahan menggunakan aplikasi <i>Mozila Firefox</i> dan mengetik alamat 
    "localhost/Toko" (Tanpa tanda kutip). Apabila proses pada tahap sebelumnya berhasil maka pada halaman perambahan 
    tersebut akan tampil halaman beranda dari webaplication tersebut.
</p>